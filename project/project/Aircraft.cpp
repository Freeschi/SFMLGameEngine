Aircraft::Aircraft(Type type) : mType(type), Entity("base_aircraft")
{
	if (type == Aircraft::Eagle)
	{
		SetValue("AircraftType", "eagle");
	}
	else {
		SetValue("AircraftType", "raptor");
	}
	SetValue("AircraftTypeID", (int)type);

	printf("Aircraft::Aircraft\n");
}

