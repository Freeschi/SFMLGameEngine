int LuaFunctions::include(lua_State* L)
{
	char* currentpath = lua->DebugGetPath();
	luaL_checkstring(lua->State(), 1);

	char path[364];
	sprintf(path, "%s%s", currentpath, lua->GetString(1));
	lua->IncludeFile(path);
	return 0;
}