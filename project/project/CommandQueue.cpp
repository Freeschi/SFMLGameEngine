#include "Includes.h"

// ====================================================================================================
// CommandQueue::push
// ====================================================================================================
void CommandQueue::push(const Command& command)
{
	mQueue.push(command);
}

// ====================================================================================================
// CommandQueue::pop
// ====================================================================================================
Command CommandQueue::pop()
{
	Command command = mQueue.front();
	mQueue.pop();
	return command;
}

// ====================================================================================================
// CommandQueue::isEmpty
// ====================================================================================================
bool CommandQueue::isEmpty() const
{
	return mQueue.empty();
}
